     */
    public function index()
    {
        //
        //return all users
        $auth_user = Auth::user()->id;
        $users = DB::table('users')->where('id', '!=', $auth_user)->orderBy('id','DESC')->paginate(10);
        return view('user.users', ['users'=>$users]);
